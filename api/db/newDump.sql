CREATE TABLE `book` (
	`id` INT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,
	`title` CHAR(255) CHARACTER SET utf8 NOT NULL,
	`user_id` INT UNSIGNED NOT NULL
);

CREATE TABLE `user` (
	`id` INT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,
	`username` CHAR(255) CHARACTER SET utf8 NOT NULL,
	`password` CHAR(255) CHARACTER SET utf8 NOT NULL
);

CREATE TABLE `sentence` (
	`id` INT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,
	`book_id` INT UNSIGNED NOT NULL,
	`user_id` INT UNSIGNED NOT NULL,
	`translation` CHAR(255) CHARACTER SET utf8 NOT NULL,
	`note` CHAR(255) CHARACTER SET utf8 NOT NULL
);


ALTER TABLE `book` ADD FOREIGN KEY (`user_id`) REFERENCES `user` (`id`);
ALTER TABLE `sentence` ADD FOREIGN KEY (`book_id`) REFERENCES `book` (`id`);
ALTER TABLE `sentence` ADD FOREIGN KEY (`user_id`) REFERENCES `user` (`id`);
